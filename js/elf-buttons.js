// Doll Background Functions
let bckgndIndex = 2;

function showBckgnd(index) {
  if (index >= 0 && index < backgrounds.length) {
    let bckgndEl = document.getElementById("elf-bckgnd");
    bckgndEl.src = backgrounds[index];
    bckgndIndex = index;
    localStorage.setItem("elf-bckgnd", JSON.stringify(index));
  }
}

function nextBckgnd() {
  if (bckgndIndex < backgrounds.length - 1) {
    showBckgnd(bckgndIndex + 1);
  } else {
    bckgndIndex = 0;
    showBckgnd(bckgndIndex);
  }
}

function previousBckgnd() {
  if (bckgndIndex > 0) {
    showBckgnd(bckgndIndex - 1);
  } else {
    bckgndIndex = 5;
    showBckgnd(bckgndIndex);
  }
}

